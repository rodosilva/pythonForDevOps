# Create a simple Python function named greet that takes one parameter (name) and prints a greeting message

def greet(name):
    print(f"Hello, {name}!")

# Call the greet function with the names Alice, Bob, and Charlie to test it, and print a greeting message for each
greet("Alice")
greet("Bob")
greet("Charlie")

