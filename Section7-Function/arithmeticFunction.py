
# Define the addition function: Name it add, accept two parameters, a and b, and return their sum, then call the function.
def add(a, b):
    return a + b

result = add(5, 10)
print(f"The sum of 5 and 10 is {result}")

# Define the Subtraction Function: Name it subtract, accept two parameters a and b, and return the difference, then call the function.
def subtract(a, b):
    return a - b

result = subtract(10, 5)

# Define the Multiplication Function: Name it multiply, accept two parameters a and b, and return the product, then call the function.
def multiply(a, b):
    return a * b

result = multiply(5, 10)

# Define the Division Function: Name it divide, accept two parameters a and b, and return the quotient of a divided by b, then call the function.
def divide(a, b):
    return a / b

result = divide(10, 5)