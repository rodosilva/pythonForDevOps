
favorite_movies = ("Infinity Castle", "Avatar", "Endgame", "I Robot", "Karate Kid Legends")

# Print the entire favorite_movies tuple
print(favorite_movies)

# Print the first movie in the tuple by accessing it with the index 0
print(favorite_movies[0])

# Print the last movie in the tuple using the index -1
print(favorite_movies[-1])

# Use the len() function to find and print the total number of movies in the favorite_movies tuple
print(len(favorite_movies))

# Print all the movies in the new_favorite_movies tuple, each on a new line. You can use a for loop to iterate through the tuple
print("All favorite movies:")
for movie in favorite_movies:
    print(movie)



